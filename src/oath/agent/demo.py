"""Autonomous demo orchestrator — what the agent actually does end-to-end.

This module backs the `oath demo` CLI subcommand. The operator types ONE
command (`oath demo`) and walks away. The orchestrator drives the agent
through eight scenes against real signed envelopes from
`logs/sample-run/dlc-sample-run.jsonl`:

  0. Banner
  1. Mount confirmation (handle SHA-256, image size, mount tech)
  2..7. Six typed-function calls — real signed envelopes streamed in order
  8. A real Ralph Wiggum self-correction event triggered by the production
     `WitnessOathVerifier` rejecting a *tampered* copy of one envelope's
     persisted data (data_blake3 mismatch — the same attack the spoliation
     tests exercise). The agent visibly abandons the bad claim and re-proposes
     citing a different envelope.
  9. A QUARANTINED verdict (claim cites a record predicate that no record
     matches) so the viewer sees what hallucination-made-visible looks like.
 10. A VERIFIED verdict over the five surviving envelopes.
 11. The shipped finding with its replay receipt.

Nothing here is hand-faked. The Ralph Wiggum event is generated by
`RalphWiggumLoop.run()` over the real `WitnessOathVerifier` and a forgiving
re-verify registry that mirrors the spoliation-test attack premise: raw
stdout BLAKE3 still matches (because we don't actually run the tool), but
data_blake3 fails because we mutated the persisted records in memory.

This is what the demo-video screencast records.
"""
